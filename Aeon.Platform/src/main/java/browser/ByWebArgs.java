package browser;

/**
 * Class to define IByWeb arguments.
 */
public class ByWebArgs {

    private String selector;
    private String type;

    /**
     * Get the selector.
     * @return Selector
     */
    public String getSelector() {
        return selector;
    }

    /**
     * Get the type.
     * @return Type
     */
    public String getType() {
        return type;
    }
}
